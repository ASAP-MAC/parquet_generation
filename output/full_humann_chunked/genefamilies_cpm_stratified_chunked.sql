    list_transform(getvariable('sample_prefixes'), lambda x : concat(x, '/humann/out_genefamilies_cpm_stratified.tsv.gz')),
    filename=True,
    auto_detect=False,
    columns=getvariable('genefamilies_cpm_stratified_columns'),
    delim='\t',
    skip=0,
    store_rejects=True,
    rejects_scan='genefamilies_cpm_stratified_2_scan',
    rejects_table='genefamilies_cpm_stratified_2_errors');

CREATE OR REPLACE TABLE genefamilies_cpm_stratified_2_headers AS
WITH deduped_lines AS (
    SELECT DISTINCT scan_id, file_id, line, csv_line
    FROM genefamilies_cpm_stratified_2_errors
)
SELECT
    scan_id,
    file_id,
    string_agg(csv_line, '|' ORDER BY line) AS full_header,
    struct_pack(
        humann_header := split_part(full_header, '|', 1)
    ) AS nested_header
FROM deduped_lines
GROUP BY scan_id, file_id;

CREATE OR REPLACE TABLE genefamilies_cpm_stratified_2_joined AS SELECT
    string_split_regex(t.gene_family, '[|.]')[1] AS gene_family_uniref,
    string_split_regex(t.gene_family, '[|.]')[2] AS gene_family_genus,
    string_split_regex(t.gene_family, '[|.]')[3] AS gene_family_species,
    t.* EXCLUDE (t.filename),
    split_part(t.filename, '/', 6) AS uuid,
    UNNEST(h.nested_header)
FROM genefamilies_cpm_stratified_2 AS t
INNER JOIN genefamilies_cpm_stratified_2_scan AS s ON t.filename = s.file_path
INNER JOIN genefamilies_cpm_stratified_2_headers AS h ON s.file_id = h.file_id;

